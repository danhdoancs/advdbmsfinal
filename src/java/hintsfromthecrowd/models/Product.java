package hintsfromthecrowd.models;

/**
 *
 * @author david
 */
public class Product extends java.lang.Object {

    public String productName;
    public String productLink;
    public float productRank;
    public double score;
    
    public String getProductName() {
        return productName;
    }
    
    public String getProductLink() {
        return productLink;
    }
    
    public float getProductRank() {
        return productRank;
    }
    
    public String getScore() {
        return productName;
    }
}
